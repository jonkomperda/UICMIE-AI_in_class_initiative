    function onRunSimulation(~, ~)
        try
            params = readSimulationParameters();
            ambientProfile = buildAmbientProfile();
            results = simulateControllers(ambientProfile, params);
        catch exception
            setStatus("simulation blocked");
            logMessage(exception.message);
            return;
        end

        state.lastResults = results;
        refreshResultsPlot();
        updateMetricsBox();

        setStatus("simulation complete");
        if results.thermostatEnabled && results.pidEnabled
            logMessage(sprintf( ...
                "Simulation complete for %s. Thermostat avg %.1f degF, PID avg %.1f degF (%s mode).", ...
                ambientProfile.name, results.thermostatAverageIndoor, results.pidAverageIndoor, results.pidModeLabel));
        elseif results.thermostatEnabled
            logMessage(sprintf( ...
                "Simulation complete for %s. Thermostat avg %.1f degF.", ...
                ambientProfile.name, results.thermostatAverageIndoor));
        else
            logMessage(sprintf( ...
                "Simulation complete for %s. PID avg %.1f degF (%s mode).", ...
                ambientProfile.name, results.pidAverageIndoor, results.pidModeLabel));
        end
    end

    function onFigureClick(~, ~)
        if ~state.isSelecting || ~ishandle(state.fig)
            return;
        end

        clickedObject = hittest(state.fig);
        clickedAxes = ancestor(clickedObject, "axes");
        if isempty(clickedAxes) || clickedAxes ~= state.ambientAxes
            return;
        end

        currentPoint = get(state.ambientAxes, "CurrentPoint");
        xPoint = currentPoint(1, 1);
        yPoint = currentPoint(1, 2);
        xLimits = get(state.ambientAxes, "XLim");
        yLimits = get(state.ambientAxes, "YLim");

        if xPoint < xLimits(1) || xPoint > xLimits(2) || yPoint < yLimits(1) || yPoint > yLimits(2)
            return;
        end

        xPoint = min(max(xPoint, 0), 24);
        state.profilePoints = appendOrReplacePoint(state.profilePoints, [xPoint yPoint]);
        state.profileSource = "manual";
        state.currentProfileName = "Custom";
        state.lastResults = [];
        refreshAmbientPlot();
        refreshResultsPlot();
        updateMetricsBox();
        setStatus("manual profile updated");
        logMessage(sprintf("Stored ambient point at t = %.2f hr, T = %.1f degF.", xPoint, yPoint));
    end

    function onCloseFigure(~, ~)
