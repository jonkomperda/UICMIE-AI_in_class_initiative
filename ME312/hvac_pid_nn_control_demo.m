            "LineWidth", 1.8, ...
            "Color", state.colors.outdoor);
        hold(state.resultsAxes, "on");
        indoorHandle = plot( ...
            state.resultsAxes, nan, nan, ...
            "-", ...
            "LineWidth", 2.0, ...
