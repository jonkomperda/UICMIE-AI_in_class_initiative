                "HorizontalAlignment", "center", ...
                "FontSize", 11, ...
                "Color", [0.25 0.25 0.25]);
            return;
        end

        axes(state.resultsAxes);
        yyaxis(state.resultsAxes, "left");
        ambientHandle = plot( ...
            state.resultsAxes, nan, nan, ...
