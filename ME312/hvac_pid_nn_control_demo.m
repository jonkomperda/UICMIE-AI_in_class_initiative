    function points = appendOrReplacePoint(points, newPoint)
        if isempty(points)
            points = newPoint;
            return;
        end

        timeTolerance = 0.15;
        [closestDistance, closestIndex] = min(abs(points(:, 1) - newPoint(1)));
        if closestDistance <= timeTolerance
            points(closestIndex, :) = newPoint;
        else
            points(end + 1, :) = newPoint;
        end
    end

    function params = readSimulationParameters()
        params = struct();
        params.enableThermostat = logical(get(state.controls.thermostatEnabled, "Value"));
        params.enablePid = logical(get(state.controls.pidEnabled, "Value"));
        params.setpoint = readScalarField(state.controls.setpoint, "Setpoint");
        params.deadband = readScalarField(state.controls.deadband, "Deadband");
        params.initialIndoor = readScalarField(state.controls.initialIndoor, "Initial indoor temperature");
        params.roomResponseRate = readScalarField(state.controls.roomRate, "Room response rate");
        params.coolingStrength = readScalarField(state.controls.coolingStrength, "AC cooling strength");
        params.pidKp = readScalarField(state.controls.pidKp, "Kp");
        params.pidKi = readScalarField(state.controls.pidKi, "Ki");
        params.pidKd = readScalarField(state.controls.pidKd, "Kd");
        params.pidModeIndex = get(state.controls.pidMode, "Value");
        params.pidModeLabel = getSelectedPopupString(state.controls.pidMode);

        if ~params.enableThermostat && ~params.enablePid
            error("Enable at least one controller before running the simulation.");
        end

        if params.deadband <= 0
            error("Deadband must be positive.");
        end

        if params.roomResponseRate <= 0
            error("Room response rate must be positive.");
        end

        if params.coolingStrength <= 0
            error("AC cooling strength must be positive.");
        end

        if params.pidKp < 0 || params.pidKi < 0 || params.pidKd < 0
            error("Kp, Ki, and Kd must be zero or positive.");
        end
    end

