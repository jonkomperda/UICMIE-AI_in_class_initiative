    function loadExampleProfile(exampleIndex)
        exampleProfile = state.exampleLibrary(exampleIndex);
        state.profilePoints = exampleProfile.points;
        state.profileSource = "example";
        state.currentProfileName = exampleProfile.name;
        state.lastResults = [];
        refreshAmbientPlot();
        refreshResultsPlot();
        updateMetricsBox();
        setStatus("loaded example");
        logMessage(sprintf("Loaded ambient profile: %s.", exampleProfile.name));
    end

    function onSelectModeChanged(~, ~)
        state.isSelecting = logical(get(state.controls.selectButton, "Value"));
        if state.isSelecting
            setStatus("selecting ambient points");
            logMessage("Point-selection mode enabled. Click inside the ambient plot to add or replace points.");
        else
            setStatus("idle");
            logMessage("Point-selection mode disabled.");
        end
    end

    function onUndoPoint(~, ~)
        if isempty(state.profilePoints)
            setStatus("nothing to undo");
            logMessage("Undo ignored because no ambient profile points are available.");
            return;
        end

        state.profilePoints(end, :) = [];
        state.profileSource = "manual";
        state.currentProfileName = "Custom";
        state.lastResults = [];
        refreshAmbientPlot();
        refreshResultsPlot();
        updateMetricsBox();

        if isempty(state.profilePoints)
            setStatus("profile cleared");
            logMessage("Removed the last point. No ambient profile points remain.");
        else
            setStatus("removed last point");
            logMessage("Removed the most recently stored ambient profile point.");
        end
    end

    function onClearProfile(~, ~)
        state.profilePoints = zeros(0, 2);
        state.profileSource = "manual";
        state.currentProfileName = "Custom";
        state.lastResults = [];
        refreshAmbientPlot();
        refreshResultsPlot();
        updateMetricsBox();
        setStatus("profile cleared");
        logMessage("Cleared the ambient profile and previous simulation results.");
    end

    function onRunSimulation(~, ~)
        try
            params = readSimulationParameters();
            ambientProfile = buildAmbientProfile();
            results = simulateRoomResponse(ambientProfile, params);
        catch exception
            setStatus("simulation blocked");
            logMessage(exception.message);
            return;
        end

        state.lastResults = results;
        refreshResultsPlot();
        updateMetricsBox();

        minIndoor = min(results.indoorTemperature);
        maxIndoor = max(results.indoorTemperature);
        dutyCycle = 100 * mean(results.acState);
        onTimeHours = results.acOnTimeHours;
        setStatus("simulation complete");
        logMessage(sprintf( ...
            "Simulation complete for %s. Indoor range %.1f to %.1f degF, AC duty cycle %.0f%%, AC on for %.2f hr.", ...
            ambientProfile.name, minIndoor, maxIndoor, dutyCycle, onTimeHours));
    end

    function onFigureClick(~, ~)
        if ~state.isSelecting || ~ishandle(state.fig)
            return;
        end

        clickedObject = hittest(state.fig);
        clickedAxes = ancestor(clickedObject, "axes");
        if isempty(clickedAxes) || clickedAxes ~= state.ambientAxes
            return;
        end

        currentPoint = get(state.ambientAxes, "CurrentPoint");
        xPoint = currentPoint(1, 1);
        yPoint = currentPoint(1, 2);
        xLimits = get(state.ambientAxes, "XLim");
        yLimits = get(state.ambientAxes, "YLim");

        if xPoint < xLimits(1) || xPoint > xLimits(2) || yPoint < yLimits(1) || yPoint > yLimits(2)
            return;
        end

        xPoint = min(max(xPoint, 0), 24);
        state.profilePoints = appendOrReplacePoint(state.profilePoints, [xPoint yPoint]);
        state.profileSource = "manual";
        state.currentProfileName = "Custom";
        state.lastResults = [];
        refreshAmbientPlot();
        refreshResultsPlot();
        updateMetricsBox();
        setStatus("manual profile updated");
        logMessage(sprintf("Stored ambient point at t = %.2f hr, T = %.1f degF.", xPoint, yPoint));
    end

    function onCloseFigure(~, ~)
        if ishandle(state.fig)
            delete(state.fig);
        end
    end

    function points = appendOrReplacePoint(points, newPoint)
        if isempty(points)
            points = newPoint;
            return;
        end

        timeTolerance = 0.15;
        [closestDistance, closestIndex] = min(abs(points(:, 1) - newPoint(1)));
        if closestDistance <= timeTolerance
            points(closestIndex, :) = newPoint;
        else
            points(end + 1, :) = newPoint;
        end
    end

    function params = readSimulationParameters()
        params = struct();
        params.setpoint = readScalarField(state.controls.setpoint, "Setpoint");
        params.deadband = readScalarField(state.controls.deadband, "Deadband");
        params.initialIndoor = readScalarField(state.controls.initialIndoor, "Initial indoor temperature");
        params.roomResponseRate = readScalarField(state.controls.roomRate, "Room response rate");
        params.coolingStrength = readScalarField(state.controls.coolingStrength, "AC cooling strength");

        if params.deadband <= 0
            error("Deadband must be positive.");
        end

        if params.roomResponseRate <= 0
            error("Room response rate must be positive.");
        end

        if params.coolingStrength <= 0
            error("AC cooling strength must be positive.");
        end
    end

    function numericValue = readScalarField(controlHandle, fieldLabel)
        numericValue = str2double(get(controlHandle, "String"));
        if ~isfinite(numericValue)
            error("%s must be a valid numeric value.", fieldLabel);
        end
    end

    function profile = buildAmbientProfile()
        if isempty(state.profilePoints)
            error("Create an ambient profile by loading an example or adding at least two points.");
        end

        [pointTimes, pointTemps] = sanitizeProfilePoints(state.profilePoints);
        if numel(pointTimes) < 2
            error("At least two unique time points are required to define an ambient profile.");
        end

        if pointTimes(1) > 0
            pointTimes = [0; pointTimes];
            pointTemps = [pointTemps(1); pointTemps];
        end

        if pointTimes(end) < 24
            pointTimes = [pointTimes; 24];
            pointTemps = [pointTemps; pointTemps(end)];
        end

        ambientTemperature = interp1(pointTimes, pointTemps, state.timeHours, "pchip");

        profile = struct();
        profile.timeHours = state.timeHours;
        profile.temperature = ambientTemperature(:).';
        profile.controlTimes = pointTimes;
        profile.controlTemps = pointTemps;
        profile.name = char(state.currentProfileName);
        profile.source = char(state.profileSource);
    end

    function [pointTimes, pointTemps] = sanitizeProfilePoints(rawPoints)
        sortedPoints = sortrows(rawPoints, 1);
        sortedTimes = sortedPoints(:, 1);
        sortedTemps = sortedPoints(:, 2);
        [pointTimes, uniqueIndices] = unique(sortedTimes, "last");
        pointTemps = sortedTemps(uniqueIndices);
    end

    function results = simulateRoomResponse(ambientProfile, params)
        timeHours = ambientProfile.timeHours;
        dtHours = timeHours(2) - timeHours(1);
        indoorTemperature = zeros(size(timeHours));
        acState = zeros(size(timeHours));
        indoorTemperature(1) = params.initialIndoor;

        controllerMode = "onoff";
        controllerState = struct("acOn", false);

        for index = 1:(numel(timeHours) - 1)
            [acCommand, controllerState] = updateControllerAction( ...
                controllerMode, controllerState, indoorTemperature(index), params);
            acState(index) = double(acCommand);

            temperatureSlope = params.roomResponseRate * ...
                (ambientProfile.temperature(index) - indoorTemperature(index)) - ...
                params.coolingStrength * double(acCommand);

            indoorTemperature(index + 1) = indoorTemperature(index) + dtHours * temperatureSlope;
        end

        [acCommand, controllerState] = updateControllerAction( ...
            controllerMode, controllerState, indoorTemperature(end), params);
        acState(end) = double(acCommand);

        results = struct();
        results.timeHours = timeHours;
        results.ambientTemperature = ambientProfile.temperature;
        results.indoorTemperature = indoorTemperature;
        results.setpoint = params.setpoint * ones(size(timeHours));
        results.acState = acState;
        results.acOnTimeHours = sum(acState) * dtHours;
        results.controllerMode = char(controllerMode);
        results.profileName = ambientProfile.name;
    end

    function [acCommand, controllerState] = updateControllerAction(controllerMode, controllerState, indoorTemperature, params)
        switch lower(char(controllerMode))
            case "onoff"
                upperThreshold = params.setpoint + 0.5 * params.deadband;
                lowerThreshold = params.setpoint - 0.5 * params.deadband;

                if indoorTemperature >= upperThreshold
                    controllerState.acOn = true;
                elseif indoorTemperature <= lowerThreshold
                    controllerState.acOn = false;
                end

                acCommand = controllerState.acOn;

            otherwise
                error("Controller mode '%s' is not implemented in this version.", char(controllerMode));
        end
    end

    function refreshAmbientPlot()
        cla(state.ambientAxes);
        hold(state.ambientAxes, "on");
        grid(state.ambientAxes, "on");
        xlim(state.ambientAxes, [0 24]);

        if isempty(state.profilePoints)
            ylim(state.ambientAxes, [60 110]);
            title(state.ambientAxes, "Ambient Temperature Profile");
            text( ...
                state.ambientAxes, 12, 85, ...
                {"Load a profile or click points", "to create a custom ambient curve."}, ...
                "HorizontalAlignment", "center", ...
                "FontSize", 11, ...
                "Color", [0.25 0.25 0.25]);
            hold(state.ambientAxes, "off");
            return;
        end

        try
            ambientProfile = buildAmbientProfile();
            lineHandle = plot( ...
                state.ambientAxes, ambientProfile.timeHours, ambientProfile.temperature, ...
                "LineWidth", 2.1, ...
                "Color", state.colors.outdoor);

            pointHandle = scatter( ...
                state.ambientAxes, ambientProfile.controlTimes, ambientProfile.controlTemps, ...
                50, ...
                "filled", ...
                "MarkerFaceColor", [0.85 0.33 0.10], ...
                "MarkerEdgeColor", [0.20 0.20 0.20]);

            ylim(state.ambientAxes, computeTemperatureLimits(ambientProfile.controlTemps));
            title(state.ambientAxes, sprintf("Ambient Temperature Profile: %s", state.currentProfileName));
            legend(state.ambientAxes, [lineHandle pointHandle], {"Interpolated Profile", "Control Points"}, ...
                "Location", "northwest");
        catch
            sortedPoints = sortrows(state.profilePoints, 1);
            plot( ...
                state.ambientAxes, sortedPoints(:, 1), sortedPoints(:, 2), ...
                "o--", ...
                "LineWidth", 1.4, ...
                "Color", state.colors.outdoor, ...
                "MarkerFaceColor", [0.85 0.33 0.10]);
            ylim(state.ambientAxes, computeTemperatureLimits(sortedPoints(:, 2)));
            title(state.ambientAxes, "Ambient Temperature Profile");
        end

        xlabel(state.ambientAxes, "Time (hours)");
        ylabel(state.ambientAxes, "Ambient Temperature (degF)");
        hold(state.ambientAxes, "off");
    end

