            set(acHandle, ...
                "XData", state.lastResults.timeHours, ...
                "YData", state.lastResults.acState);
        end
        hold(state.resultsAxes, "off");
    end

    function updateMetricsBox()
        if isempty(state.lastResults)
            set(state.controls.metricsBox, "String", ...
                {"AC Runtime: -- hr", "Duty Cycle: -- %", "Avg Indoor: -- degF"});
            return;
        end

        set(state.controls.metricsBox, ...
            "String", { ...
            sprintf("AC Runtime: %.2f hr", state.lastResults.acOnTimeHours), ...
            sprintf("Duty Cycle: %.0f %%", 100 * mean(state.lastResults.acState)), ...
            sprintf("Avg Indoor: %.1f degF", mean(state.lastResults.indoorTemperature))});
    end

    function animateResultsPlot(ambientHandle, indoorHandle, setpointHandle, acHandle)
        totalPoints = numel(state.lastResults.timeHours);
        frameCount = min(totalPoints, 60);
        frameIndices = unique(round(linspace(1, totalPoints, frameCount)));
        pauseTime = 3 / max(numel(frameIndices), 1);

        for frameIndex = 1:numel(frameIndices)
            pointIndex = frameIndices(frameIndex);

            set(ambientHandle, ...
                "XData", state.lastResults.timeHours(1:pointIndex), ...
                "YData", state.lastResults.ambientTemperature(1:pointIndex));
            set(indoorHandle, ...
                "XData", state.lastResults.timeHours(1:pointIndex), ...
                "YData", state.lastResults.indoorTemperature(1:pointIndex));
            set(setpointHandle, ...
                "XData", state.lastResults.timeHours(1:pointIndex), ...
                "YData", state.lastResults.setpoint(1:pointIndex));
            set(acHandle, ...
                "XData", state.lastResults.timeHours(1:pointIndex), ...
                "YData", state.lastResults.acState(1:pointIndex));

            drawnow;
            pause(pauseTime);
        end
    end

    function limits = computeTemperatureLimits(values)
        values = values(:);
        if isempty(values)
            limits = [60 100];
            return;
        end

        valueRange = max(values) - min(values);
        margin = max(4, 0.15 * max(valueRange, 1));
        limits = [min(values) - margin, max(values) + margin];
    end

